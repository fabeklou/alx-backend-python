#!/usr/bin/env python3

"""This module contains a concat function that
taks in two strings, concatenate them and
return the result
"""


def concat(string_a: str, string_b: str) -> str:
    """taks in two strings, concatenate them
    and return the result

    Args:
        string_a (str): first string
        string_b (str): second string

    Returns:
        str: result of the concatenation of
            string_a and string_b
    """
    return string_a + string_b
