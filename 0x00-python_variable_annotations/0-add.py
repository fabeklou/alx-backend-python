#!/usr/bin/env python3

"""This module contains an add function thatcadd two
floating point values together and retrun the result
"""


def add(a: float, b: float) -> float:
    """add two floating point values together and
    retrun the result

    Args:
        a (float): first value
        b (float): second value

    Returns:
        float: result of a + b
    """
    return a + b
