#!/usr/bin/env python3

"""This module contains a to_str function that receives
an argument of type float and returns his string representation
"""


def to_str(number: float) -> str:
    """receives an argument of type float
    and returns his string representation

    Args:
        number (float): the floating point number
            to be converted into string

    Returns:
        str: string representation of the given number
    """
    return str(number)
