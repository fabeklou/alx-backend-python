#!/usr/bin/env python3

"""This module contains a floor function
that taks in a floating point number and
returns his floor
"""


def floor(number: float) -> int:
    """taks in a floating point number
    and returns his floor

    Args:
        number (float): the floating point number
            which floor to return

    Returns:
        int: floor of the given number
    """
    return int(number)
